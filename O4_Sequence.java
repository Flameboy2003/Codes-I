package Paiyalagam;

        /*
          1 * 10
          2 * 9
          3 * 8
          4 * 7
          5 * 6
        */
public class O4_Sequence {
    public static void main(String[] args) {
        int i=1;
        int j=10;

        while(i<=j){
            System.out.print(" "+ i*j);
            i++;
            j--;
        }
    }
}
